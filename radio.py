def hackerlandRadioTransmitters(x, k):
    # Write your code here
    x.sort()
    minimum = x[0]
    maximum = x[len(x)-1]
    
    range = k * 2
    
    diff = maximum - minimum
    return math.ceil(diff/range)
    
